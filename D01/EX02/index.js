function isBiggerThan(x) {
  if (x > 4) {
    console.log("The number " + x + " is bigger than 4");
  } else if (x < 4){
    console.log("The number " + x + " is less than 4");
  }
  
  else if (x = 4) {
    console.log("The number " + x + " is equal to 4");
  } 
}

